    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // harus pilih semua
        if (!(jRadioButton21.isSelected() || jRadioButton22.isSelected() || jRadioButton23.isSelected())) {
            JOptionPane.showMessageDialog(this, "PChoose one of the pizza menus!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!(jCheckBox1.isSelected() || jCheckBox2.isSelected() || jCheckBox3.isSelected() ||
            jCheckBox4.isSelected() || jCheckBox5.isSelected() || jCheckBox6.isSelected())) {
        JOptionPane.showMessageDialog(this, "Select at least one topping!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
        }

        // Validasi jumlah pizza > 0
        int jumlahPizza = (int) daySpinner.getValue();
        if (jumlahPizza <= 0) {
            JOptionPane.showMessageDialog(this, "Enter the correct number of pizzas!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!(jRadioButton4.isSelected() || jRadioButton5.isSelected())) {
            JOptionPane.showMessageDialog(this, "Select Crust!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (jComboBox1.getSelectedIndex() == -1 || jComboBox1.getSelectedItem().toString().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Select the type of sauce!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (jList1.getSelectedValuesList().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Select the payment method!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // reset pesanan
        S1 = "";
        int hargaSize = 0;
        if (jRadioButton21.isSelected()) {
            S1 += "Size: " + jRadioButton21.getText() + "\n";
            hargaSize = 50000;
        }
        if (jRadioButton22.isSelected()) {
            S1 += "Size: " + jRadioButton22.getText() + "\n";
            hargaSize = 75000;
        }
        if (jRadioButton23.isSelected()) {
            S1 += "Size: " + jRadioButton23.getText() + "\n";
            hargaSize = 100000;
        }

        // Hitung topping
        int jumlahTopping = 10000;
        S1 += "Toppings:\n";
        if(jCheckBox1.isSelected()) { S1 += "- " + jCheckBox1.getText() + "\n"; jumlahTopping++; }
        if(jCheckBox2.isSelected()) { S1 += "- " + jCheckBox2.getText() + "\n"; jumlahTopping++; }
        if(jCheckBox3.isSelected()) { S1 += "- " + jCheckBox3.getText() + "\n"; jumlahTopping++; }
        if(jCheckBox4.isSelected()) { S1 += "- " + jCheckBox4.getText() + "\n"; jumlahTopping++; }
        if(jCheckBox5.isSelected()) { S1 += "- " + jCheckBox5.getText() + "\n"; jumlahTopping++; }
        if(jCheckBox6.isSelected()) { S1 += "- " + jCheckBox6.getText() + "\n"; jumlahTopping++; }

        // saus
        if (jComboBox1.getSelectedIndex() != -1) {
            S1 += "Jenis Saus: " + jComboBox1.getSelectedItem().toString() + "\n";
        }

        // metode pembayaran
        for (Object item : jList1.getSelectedValuesList()) {
            S1 += "Pembayaran: " + item.toString() + "\n";
        }

        // level pedas
        S1 += "Level Pedas: " + temperaturScrollBar.getValue() + "\n";

        // pizza jenis dan jumlah
        S1 += "Jenis Pizza: " + monthSpinner.getValue() + "\n";
        S1 += "Jumlah: " + jumlahPizza + "\n";

        // total cost
        int hargaTopping = jumlahTopping * 5;
        int totalPerPizza = hargaSize + hargaTopping;
        int totalCost = totalPerPizza * jumlahPizza;
        
        formUser = new form_user(this); // simpan sebagai variabel global
        formUser.setVisible(true);

        
        
        
        //        S1 += "Total Harga = Rp. " + totalCost + "\n"; // tampilkan total di TA

//        JOptionPane.showMessageDialog(this, "Order was successfully placed!");
        //        TA.setText(S1);
        TAA.setText(S1);
        NumberFormat formatter = NumberFormat.getNumberInstance(new Locale("id", "ID"));
        String formattedCost = "Rp. " + formatter.format(totalCost);
        //        S1 += "Total Harga = Rp. " + formattedCost + "\n";
        totalCostTextField.setText(formattedCost);
        totalCostTextField.setEditable(false);
//        addressTextArea.setText(jTextArea1.getText());
        addressTextArea.setEditable(false);
        
        
    }//GEN-LAST:event_jButton1ActionPerformed


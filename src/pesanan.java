    private void Konversi() {
    // Mendapatkan nilai dari JScrollBar
    int level = temperaturScrollBar.getValue();
    FTextFiled.setText(" " + level);

    // Atur warna berdasarkan level 0–10
    if (level == 0) {
        panelNya.setBackground(new Color(204, 255, 204)); // Hijau sangat muda
    } else if (level == 1) {
        panelNya.setBackground(new Color(178, 255, 178)); // Hijau lebih muda
    } else if (level == 2) {
        panelNya.setBackground(new Color(153, 255, 153)); // Hijau muda
    } else if (level == 3) {
        panelNya.setBackground(new Color(127, 255, 127)); // Hijau sedang
    } else if (level == 4) {
        panelNya.setBackground(new Color(255, 204, 102)); // Oranye muda
    } else if (level == 5) {
        panelNya.setBackground(new Color(255, 178, 51)); // Oranye sedang
    } else if (level == 6) {
        panelNya.setBackground(new Color(255, 153, 51)); // Oranye tua
    } else if (level == 7) {
        panelNya.setBackground(new Color(255, 102, 0)); // Oranye pekat
    } else if (level == 8) {
        panelNya.setBackground(new Color(255, 51, 51)); // Merah muda
    } else if (level == 9) {
        panelNya.setBackground(new Color(255, 26, 26)); // Merah sedang
    } else if (level == 10) {
        panelNya.setBackground(new Color(204, 0, 0)); // Merah pekat
    } else {
        panelNya.setBackground(Color.GRAY); // Jika di luar jangkauan
    }
}
    

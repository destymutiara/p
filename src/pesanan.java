    private void monthSpinnerStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_monthSpinnerStateChanged
        // TODO add your handling code here:
        String warna = (String) monthSpinner.getValue();
        String fileName;

        switch (warna) {
            case "Spaghetti Bolognese":
            fileName = "Spaghetti1.jpg";
            break;
            case "Spaghetti Carbonara":
            fileName = "Spaghetti2.jpg";
            break;
            case "Spaghetti Aglio e Olio":
            fileName = "Spaghetti3.jpg";
            break;
            case "Spaghetti Marinara":
            fileName = "Spaghetti4.jpg";
            break;
            case "Spaghetti Pesto":
            fileName = "Spaghetti5.jpg";
            break;
            default:
            fileName = "Spaghetti Marinara.jpg"; // ← Konsisten: dulunya "marghetita.jpeg", typo & beda ekstensi
            break;
        }

        java.net.URL imgUrl = getClass().getResource("/imageicon/newpackage/" + fileName);

        if (imgUrl != null) {
            gambar.setIcon(new javax.swing.ImageIcon(imgUrl));
        } else {
            System.err.println("Gambar tidak ditemukan: " + fileName);
        }
        //        TA.setText("Pizza : " + monthSpinner.getValue() + " " + daySpinner.getValue());
    }//GEN-LAST:event_monthSpinnerStateChanged

